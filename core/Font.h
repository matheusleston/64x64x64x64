// Desenha um caractere com canto superior-esquerdo em (x,y). Fora da faixa
// imprimível vira espaço. fb.set já faz o clip nas bordas. Com reveal < 1 os
// pixels passam pela máscara de dithering (dissolve por limiar fixo).
inline void drawChar(Framebuffer& fb, int x, int y, char c, Rgb col,
                     float reveal = 1.f) {
  unsigned idx = static_cast<unsigned char>(c) - 0x20u;
  if (idx >= 95u) idx = 0;
  const uint8_t* g = kFont4x6 + idx * kFontW;
  for (int cx = 0; cx < kFontW; ++cx) {
    uint8_t bits = g[cx];
    for (int cy = 0; cy < kFontH; ++cy)
      if ((bits & (1u << cy)) && ditherOn(x + cx, y + cy, reveal))
        fb.set(x + cx, y + cy, col);
  }
}

// Desenha uma linha de texto (até '\0' ou '\n') a partir de (x,y).
inline void drawText(Framebuffer& fb, int x, int y, const char* s, Rgb col,
                     float reveal = 1.f) {
  for (; *s && *s != '\n'; ++s) {
    drawChar(fb, x, y, *s, col, reveal); x += kCharAdv;
  }
}

// Desenha texto alinhado à esquerda a partir de (x,y); '\n' quebra linha.
inline void drawTextLeft(Framebuffer& fb, int x, int y, const char* s, Rgb col,
                         float reveal = 1.f) {
  for (const char* p = s;;) {
    drawText(fb, x, y, p, col, reveal);
    while (*p && *p != '\n') ++p;
    if (!*p) break;
    ++p; y += kLineAdv;
  }
}

